class Stringfunctions {
    public static void main(String args[]) {
        String name = "Mangam Tanmayi";

        System.out.println("Substring is: " + name.substring(3, 8));
        System.out.println("Lowercase is: " + name.toLowerCase());
        System.out.println("Uppercase is: " + name.toUpperCase());
        System.out.println("Concatenation is: " + name.concat(" Student"));
    }
}